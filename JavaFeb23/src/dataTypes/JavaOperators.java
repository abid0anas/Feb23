package dataTypes;

public class JavaOperators {

	public static void main(String[] args) {
		
		/** There are 5 types of Java Operators
		 * 
		 * Arithmetic Operators
		 * Assignment Operators
		 * Comparison/Relational Operators
		 * Logical Operators
		 * Bitwise Operators
		 */
		
		
		// Learning Arithmetic Operators
		//They are +, -, *, /, %. ++, --
		
		int a = 10;
		int b = 5;
		int result;
		
		result = a + b;// addition
		System.out.println("Addition Result " +result);//15
		result = a - b;// addition
		System.out.println("Subtraction Result " +result);//5
		result = a * b;// addition
		System.out.println("Multiplication Result " +result);//50
		result = a / b;// addition
		System.out.println("Division Result " +result);//2
		result = a % b;// addition
		System.out.println("Modulus Result is " +result);//0
		System.out.println("Increment a is " + a++);//11
		System.out.println("Decrement a is " + a--);//9
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
